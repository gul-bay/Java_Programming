package day04_concatenation;

public class ArithmeticOperators {

    public static void main(String[] args) {

        System.out.println(100 + 200); // Addition: number + number

        System.out.println("100" + 200); // Concatenation: String + number

        System.out.println(40-10); //  Minus operator is only for Subtraction

        System.out.println(50/6); // Division: int/int =>int

        System.out.println(50/6.0); // int/double => double

        System.out.println(50.0/6); // double/int => double

        System.out.println(50/6); // int/int => int

        System.out.println(50d/6); // double/int => double

        System.out.println(2.5/0.5); // double/double =>double

        System.out.println((int) (2.5/0.5));

        // System.out.println(9/0); the denominator should never be zero

        System.out.println(10*2);

        System.out.println(20*12);

        System.out.println(100%13);

        System.out.println(100%10); // remainder is divisible

        System.out.println(45%8);


        long a =30L;
        short b = (short) a;

        System.out.println(b);

        System.out.println(b);






    }
}
